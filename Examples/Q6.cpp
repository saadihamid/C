 // اعداد 0 تا 5 را نمایش دهد(for) برنامه ای بنویسید که با استفاده از دستور 






#include <iostream>
using namespace std;

int main() {
    cout << "Numbers from 0 to 5 using for loop:" << endl;
    for (int i = 0; i <= 5; ++i) {
        cout << i << " ";
    }
    cout << endl;

    return 0;
}
